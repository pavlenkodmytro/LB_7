#include <iostream> // Підключаємо бібліотеку для вводу-виводу
using namespace std; // Використовуємо простір імен std для спрощення коду

int main(void) { // Головна функція програми
    int n; // Оголошуємо змінну для зберігання розміру квадрата
    cout << "Enter a positive integer value greater than 1: "; // Запитуємо користувача ввести число
    cin >> n; // Зчитуємо введене число в змінну n

    if (n <= 1) { // Перевіряємо, чи число менше або дорівнює 1
        cout << "Sorry, the side size must be greater than 1." << endl; // Повідомляємо про помилку
        return 0; // Завершуємо програму
    }

    if (n > 80) { // Перевіряємо, чи число більше 80 (розумний верхній ліміт)
        cout << "Sorry, the side size is too big." << endl; // Повідомляємо про помилку
        return 0; // Завершуємо програму
    }

    // Виводимо верхню межу квадрата
    cout << '+'; 
    for(int i = 0; i < n; i++) // Цикл для виводу горизонтальних ліній
        cout << '-'; // Додаємо символи '-'
    cout << '+' << endl; // Завершуємо рядок, виводячи праву межу

    // Виводимо бокові сторони квадрата
    for(int i = 0; i < n; i++) { // Цикл для висоти квадрата
        cout << '|'; // Виводимо ліву межу
        for(int j = 0; j < n; j++) // Цикл для ширини квадрата
            cout << ' '; // Виводимо пробіли всередині
        cout << '|' << endl; // Виводимо праву межу і завершуємо рядок
    }

    // Виводимо нижню межу квадрата
    cout << '+'; 
    for(int i = 0; i < n; i++) // Цикл для виводу горизонтальних ліній
        cout << '-'; // Додаємо символи '-'
    cout << '+' << endl; // Завершуємо рядок, виводячи праву межу

    return 0; // Завершуємо програму
}
