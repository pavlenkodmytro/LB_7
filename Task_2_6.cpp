#include <iostream> // Підключаємо бібліотеку для вводу-виводу

int main() { // Головна функція програми
    int c0; // Оголошуємо змінну для зберігання числа, введеного користувачем
    std::cout << "Enter a natural number: "; // Запитуємо користувача ввести натуральне число
    std::cin >> c0; // Зчитуємо введене число в змінну c0

    int steps = 0; // Оголошуємо змінну для підрахунку кроків
    std::cout << c0 << " "; // Виводимо початкове число

    while (c0 != 1) { // Поки число не дорівнює 1, виконуємо цикл
        if (c0 % 2 == 0) { // Якщо число парне
            c0 = c0 / 2; // Ділимо його на 2
        } else { // Якщо число непарне
            c0 = 3 * c0 + 1; // Обчислюємо 3 * число + 1
        }
        std::cout << c0 << " "; // Виводимо нове значення c0
        steps++; // Збільшуємо лічильник кроків
    }

    std::cout << "\nsteps = " << steps << std::endl; // Виводимо загальну кількість кроків

    return 0; // Завершуємо програму
}
